#include <stdio.h>

/*
Numero inteiro -> int
número de ponto flutuante -> double
um único caractere -> char (valores literais usar aspas simples neste caso)
texto -> char[]
valor lógico -> int

----As varíaveis tem um lixo de memória----

As vezes quando não damos o valor para variáveis, o computador aproveita outros valores na memória

*/

int main() {
	
	int x; /*Criando variável, primeiro o tipo (int) depois o nome (x)*/
	
	x=10; /*Atribuindo valor na variável*/
	
	printf();
	
	return 0;
}
